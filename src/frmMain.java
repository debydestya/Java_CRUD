    private void tbDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDataMouseClicked
        // TODO add your handling code here:
        int baris = tbData.getSelectedRow();
        if(baris != -1)
        {
            tfNis.setText(tbData.getValueAt(baris, 0).toString());
            tfNama.setText(tbData.getValueAt(baris, 1).toString());
            tfTempat.setText(tbData.getValueAt(baris, 2).toString());
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            Date dateFormat = null;
            try 
            {
                dateFormat = date.parse(tbData.getValueAt(baris, 3).toString());
            }
            catch (ParseException ex)
            {
                Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
            }
            Tanggal.setDate(dateFormat);
            String JK = tbData.getValueAt(baris, 4).toString();
            if (JK.equals("Laki-Laki"))
                rbLaki.setSelected(true);
            else
                rbPerempuan.setSelected(true);
            tfKelas.setText(tbData.getValueAt(baris, 5).toString());
            tfEmail.setText(tbData.getValueAt(baris, 6).toString());
            taAlamat.setText(tbData.getValueAt(baris, 7).toString());
        }
    }//GEN-LAST:event_tbDataMouseClicked

